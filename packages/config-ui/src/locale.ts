export type DictionaryKey = keyof typeof dictionary.en
export type Dictionary = Record<DictionaryKey, string>

export const [localeStore, setLocaleStore] = createStore({
  locale: detectDefaultLocale(),
})

export function setLocale(locale: Locale) {
  setLocaleStore("locale", locale)
  if (typeof window !== "undefined") {
    window.localStorage.setItem(STORAGE_KEY, locale)
  }
}

export function useDictionary() {
  const dict = createMemo<Dictionary>(() => dictionary[localeStore.locale] as Dictionary)
  const t = (key: DictionaryKey) => dict()[key]
  return { dict, t }
}
